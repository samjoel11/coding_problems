#include <iostream>

using namespace std;

int main() {
    // Declare variables to store the input integers
    int a, b, c;
    
    // Read three space-separated integers from stdin
    cin >> a >> b >> c;
    
    // Calculate the sum
    int sum = a + b + c;
    
    // Print the sum to stdout
    cout << sum << endl;
    
    return 0;
}
